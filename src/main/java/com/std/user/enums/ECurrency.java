public enum ECurrency {
    CNY("CNY", "人民币"), XNB("XNB", "积分虚拟币");

    ECurrency(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }
}
