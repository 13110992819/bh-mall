package com.bh.mall.enums;

public enum EProductIsTotal {
    NO("0", "不计入出货"), YES("1", "计入出货");

    EProductIsTotal(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
